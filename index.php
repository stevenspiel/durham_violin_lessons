	function scrollToAnchor(aid){
	    var anchor = $(aid);
	    $('html,body').animate({scrollTop: anchor.offset().top},1200,'easeInOutCubic');
	}
	$("#learn-more-button").click(function() {
	   scrollToAnchor('#philosophy');
	});

	$(document).ready(function(){
		$('.icon-container').each(function(e){
			$(this).css('line-height', $(this).siblings('.text').height() + 'px');
		});

		$('#topbar').hide()

    $('.communication-tooltip').tooltipsy({
      offset: [0, 10],
      show: function (e, $el) {
        $el.show();
        if($el.attr('id') != 'contact') {
          $('#basic-info').fadeOut();
        }
      },
      css: {
        'background': '#ffffff',
        'padding': '12px 12px 11px 10px',
        'border': '1px solid rgba(0, 0, 0, 0.2)',
        'border-radius': '4px'
      }
    });

		if ($(window).width > 622) {
			$("#basic-info").css('left', ($(window).width() / 2) - ($("#basic-info").width()/ 2) + 40) + 'px';
		} else {
			$("#basic-info").css('left', ($(window).width() / 2) - ($("#basic-info").width()/ 2) - 16) + 'px';
		}

		$(document).on('click',function(e){
			if(!$(e.target).closest('#basic-info, .contact').length) {
		        if($('#basic-info').is(":visible")) {
		            $('#basic-info').fadeOut()
		        }
		    }
		});

		$("#communicate").on('click', function(){
			$("#basic-info").fadeIn()
		})

		if ($(window).width() < 622){
			$('#pull-left-container').removeClass('pull-left-container')
			$('#pull-left').removeClass('pull-left')
			$(".icon").hide()
		} else {
			$('#pull-left-container').addClass('pull-left-container')
			$('#pull-left').addClass('pull-left')
			$(".icon").show()
		}
	});

	$(window).resize(function(){
		$('.icon-container').each(function(e){
			$(this).css('line-height', $(this).siblings('.text').height() + 'px');
		});

		if($(window).scrollTop() < ($("#main").height() * 1.15)){
			$('#topbar').hide()
		}

		if ($(window).width > 622) {
			$("#basic-info").css('left', ($(window).width() / 2) - ($("#basic-info").width()/ 2) - 40) + 'px';
		} else {
			$("#basic-info").css('left', ($(window).width() / 2) - ($("#basic-info").width()/ 2) - 16) + 'px';
		}


		if ($(window).width() < 622){
			$('#pull-left-container').removeClass('pull-left-container')
			$('#pull-left').removeClass('pull-left')
			$(".icon").hide()
		} else {
			$('#pull-left-container').addClass('pull-left-container')
			$('#pull-left').addClass('pull-left')
			$(".icon").show()
		}
	})

	$(window).scroll(function() { // when the page is scrolled run this
	    if($(this).scrollTop() != 0 && $(this).scrollTop() >= ($("#main").height() * 1.15)) { // if you're NOT at the top
	        $('#topbar').fadeIn();
	    } else { // else
	        $('#topbar').fadeOut();
	    }
	});



